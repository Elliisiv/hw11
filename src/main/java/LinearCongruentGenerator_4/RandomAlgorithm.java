package LinearCongruentGenerator_4;

public class RandomAlgorithm {
    private long a, c, m;
    public RandomAlgorithm(long a, long m,long c) {
        this.a = a;
        this.m = m;
        this.c = c;
    }

    public long getA() {
        return a;
    }

    public void setA(long a) {
        this.a = a;
    }

    public long getC() {
        return c;
    }

    public void setC(long c) {
        this.c = c;
    }

    public long getM() {
        return m;
    }

    public void setM(long m) {
        this.m = m;
    }
}
